/**
 * @param {string} repoPath Tagged fixture repository.
 * @param {string} presetFileName Published TOML filename.
 *
 * @returns {Promise<readonly [string, string]>} Filename and rendered output.
 */
const renderPresetOutput = async (repoPath, presetFileName) => {
    const arguments_ = [
        gitCliffCliPath,
        "--config",
        path.join(presetsDirectory, presetFileName),
        "--current",
        "--offline",
    ];
    const parentEnvironment = Object.fromEntries(
        Object.entries(process.env).filter(
            ([name]) => name !== "FORCE_COLOR" && name !== "NO_COLOR"
        )
    );
    const environment = {
        ...parentEnvironment,
        AZURE_DEVOPS_TOKEN: "",
        CI_PROJECT_URL: "",
        GITHUB_TOKEN: "",
        GITLAB_TOKEN: "",
        NO_COLOR: "1",
        TZ: "UTC",
    };

    if (presetFileName === "azure-devops-keepachangelog.toml") {
        arguments_.push("--azure-devops-repo", "acme/platform/widget");
    } else if (presetFileName === "gitlab-keepachangelog.toml") {
        arguments_.push("--gitlab-repo", "acme/widget");
        environment.CI_PROJECT_URL = "https://gitlab.example/acme/widget";
    } else {
        arguments_.push("--github-repo", "Nick2bad4u/example-package");
    }

    const changelog = await run(process.execPath, arguments_, repoPath, {
        env: environment,
    });

    return [presetFileName, changelog.replaceAll("\r\n", "\n")];
};

/**
 * Renders every published preset from one deterministic tagged repository.
 *
 * @returns {Promise<ReadonlyMap<string, string>>} Output keyed by TOML
 *   filename.
 */
export const generateAllPresetOutputs = async () => {
    const repoPath = await mkdtemp(
        path.join(tmpdir(), "gitcliff-preset-examples-")
    );

    try {
        await initializePresetExampleRepository(repoPath);
        const presetFileNames = await readPresetFileNames();

        return new Map(
            await Promise.all(
                presetFileNames.map(async (fileName) =>
                    renderPresetOutput(repoPath, fileName)
                )
            )
        );
    } finally {
        await rm(repoPath, {
            force: true,
            maxRetries: 5,
            recursive: true,
            retryDelay: 200,
        });
    }
};

