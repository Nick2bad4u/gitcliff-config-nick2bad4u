/**
 * @typedef {object} RunOptions
 *
 * @property {NodeJS.ProcessEnv} [env] Child-process environment.
 */

/**
 * @param {string} command Executable name or path.
 * @param {readonly string[]} arguments_ Command arguments.
 * @param {string} cwd Working directory.
 * @param {RunOptions} [options] Execution options.
 *
 * @returns {Promise<string>} Standard output.
 */
const run = async (command, arguments_, cwd, options = {}) =>
    new Promise((resolve, reject) => {
        const execOptions =
            options.env === undefined
                ? {
                      cwd,
                      encoding: /** @type {const} */ ("utf8"),
                      maxBuffer: 4 * 1024 * 1024,
                      windowsHide: true,
                  }
                : {
                      cwd,
                      encoding: /** @type {const} */ ("utf8"),
                      env: options.env,
                      maxBuffer: 4 * 1024 * 1024,
                      windowsHide: true,
                  };
        const childProcess = execFile(
            command,
            [...arguments_],
            execOptions,
            (error, stdout, stderr) => {
                if (error) {
                    reject(
                        new Error(
                            [
                                error.message,
                                stdout,
                                stderr,
                            ]
                                .filter(Boolean)
                                .join("\n"),
                            { cause: error }
                        )
                    );
                    return;
                }

                resolve(stdout);
            }
        );

        childProcess.once("error", reject);
    });

/**
 * @param {number} sequence Zero-based fixture commit sequence.
 *
 * @returns {string} Stable ISO timestamp.
 */
const getFixtureTimestamp = (sequence) =>
    new Date(Date.UTC(2025, 0, sequence + 1, 12)).toISOString();

/**
 * @param {string} repoPath Fixture repository path.
 * @param {FixtureCommit} commit Commit definition.
 * @param {number} sequence Stable commit sequence.
 *
 * @returns {Promise<void>}
 */
const commitFixture = async (repoPath, commit, sequence) => {
    await writeFile(
        path.join(repoPath, commit.fileName),
        `${commit.subject}\n`,
        "utf8"
    );
    await run(
        "git",
        [
            "add",
            "--",
            commit.fileName,
        ],
        repoPath
    );

    const arguments_ = [
        "commit",
        "--no-gpg-sign",
        "-m",
        commit.subject,
    ];

    if (commit.body !== undefined) {
        arguments_.push("-m", commit.body);
    }

    const timestamp = getFixtureTimestamp(sequence);

    await run("git", arguments_, repoPath, {
        env: {
            ...process.env,
            GIT_AUTHOR_DATE: timestamp,
            GIT_COMMITTER_DATE: timestamp,
            TZ: "UTC",
        },
    });
};

/**
 * Creates the deterministic tagged Git history used by both tests and examples.
 *
 * @param {string} repoPath Empty directory for the fixture repository.
 *
 * @returns {Promise<void>}
 */
export const initializePresetExampleRepository = async (repoPath) => {
    await run(
        "git",
        [
            "init",
            "--initial-branch",
            "main",
            "--object-format",
            "sha1",
        ],
        repoPath
    );
    await run(
        "git",
        [
            "config",
            "user.name",
            fixtureAuthorName,
        ],
        repoPath
    );
    await run(
        "git",
        [
            "config",
            "user.email",
            fixtureAuthorEmail,
        ],
        repoPath
    );
    await run(
        "git",
        [
            "config",
            "core.autocrlf",
            "false",
        ],
        repoPath
    );
    await run(
        "git",
        [
            "config",
            "core.filemode",
            "false",
        ],
        repoPath
    );
    await run(
        "git",
        [
            "config",
            "commit.gpgsign",
            "false",
        ],
        repoPath
    );
    await run(
        "git",
        [
            "config",
            "tag.gpgsign",
            "false",
        ],
        repoPath
    );

    await commitFixture(
        repoPath,
        {
            fileName: "initial.txt",
            subject: "chore: initial release",
        },
        0
    );
    await run("git", ["tag", "v1.0.0"], repoPath);

    for (const [index, commit] of releaseCommits.entries()) {
        await commitFixture(repoPath, commit, index + 1);
    }

    await commitFixture(
        repoPath,
        {
            fileName: "release.txt",
            subject: "chore(release): prepare for v1.1.0",
        },
        releaseCommits.length + 1
    );
    await run("git", ["tag", "v1.1.0"], repoPath);
};

/**
 * @returns {Promise<readonly string[]>} Sorted published preset filenames.
 */
export const readPresetFileNames = async () =>
    (await readdir(presetsDirectory))
        .filter((fileName) => fileName.endsWith(".toml"))
        .sort((left, right) => left.localeCompare(right, "en"));

/**
 * @param {string} presetFileName Published TOML filename.
 *
 * @returns {string} Matching Markdown example filename.
 */
export const getPresetExampleFileName = (presetFileName) => {
    if (!presetFileName.endsWith(".toml")) {
        throw new Error(`Expected a .toml preset filename: ${presetFileName}`);
    }

    return `${presetFileName.slice(0, -".toml".length)}.md`;
};

