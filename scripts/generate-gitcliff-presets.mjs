/**
 * @param {string} contents Full root configuration.
 * @param {string} startMarker Inclusive section marker.
 * @param {string} endMarker Exclusive section marker.
 *
 * @returns {string} Extracted configuration section.
 */
const getSection = (contents, startMarker, endMarker) => {
    const startIndex = contents.indexOf(startMarker);
    const endIndex = contents.indexOf(endMarker, startIndex);

    if (startIndex === -1 || endIndex === -1) {
        throw new Error(
            `Unable to extract shared config section ${startMarker}...${endMarker}`
        );
    }

    return contents.slice(startIndex, endIndex).trimEnd();
};

/**
 * @param {Preset["remote"]} remote Preset remote provider.
 * @param {string} githubRemote Canonical GitHub remote block.
 *
 * @returns {string} Provider-specific remote configuration.
 */
const renderRemote = (remote, githubRemote) => {
    if (remote === "github") {
        return githubRemote;
    }

    if (remote === "gitlab") {
        return `[remote]
    offline = false

    # Set GITLAB_REPO or pass --gitlab-repo owner/repo. CI_PROJECT_URL can
    # override rendered links for self-hosted GitLab instances.
    # [remote.gitlab]
    # owner = "owner"
    # repo = "repository"`;
    }

    if (remote === "azure_devops") {
        return `[remote]
    offline = false

    # Set AZURE_DEVOPS_REPO or pass --azure-devops-repo
    # organization/project/repository.
    # [remote.azure_devops]
    # owner = "organization/project"
    # repo = "repository"`;
    }

    return "";
};

/**
 * @param {Preset} preset Preset definition.
 * @param {string} sharedGit Canonical git parser block.
 *
 * @returns {string} Preset-specific git parser block.
 */
const renderGit = (preset, sharedGit) => {
    let renderedGit = sharedGit.replace(
        /sort_commits\s+=\s+"newest"/u,
        `sort_commits          = "${preset.sort}"`
    );

    if (preset.conventionalCommits === false) {
        renderedGit = renderedGit.replace(
            /conventional_commits\s+=\s+true/u,
            "conventional_commits  = false"
        );
    }

    if (preset.linkParser === true) {
        const href =
            "https://github.com/" +
            "${remote.github.owner}/${remote.github.repo}/issues/$1";
        const linkParser = String.raw`    # Extract issue references for the statistics template.
    link_parsers         = [
        { pattern = "#(\\d+)", href = "${href}" },
    ]
`;
        renderedGit = renderedGit.replace(
            "    # filter out the commits that are not matched by commit parsers",
            `${linkParser}    # filter out the commits that are not matched by commit parsers`
        );
    }

    return renderedGit;
};

/**
 * @typedef {object} RenderPresetOptions
 *
 * @property {string} githubRemote Canonical GitHub remote block.
 * @property {Preset} preset Preset definition.
 * @property {string} sharedChangelogOptions Canonical changelog options.
 * @property {string} sharedGit Canonical git parser block.
 */

/**
 * @param {RenderPresetOptions} options Preset rendering inputs.
 *
 * @returns {string} Unformatted standalone TOML configuration.
 */
const renderPreset = ({
    preset,
    sharedChangelogOptions,
    sharedGit,
    githubRemote,
}) => {
    const sourceComment =
        preset.source === undefined
            ? "# Original preset for gitcliff-config-nick2bad4u."
            : `# Adapted from ${preset.source}`;
    const remote = renderRemote(preset.remote, githubRemote);
    const sections = [
        "# This file is generated by npm run presets:generate. Do not edit it directly.",
        sourceComment,
        "# Shared parsing is derived from ../cliff.toml.",
        "# yaml-language-server: $schema=https://www.schemastore.org/git-cliff.json",
        "",
        '"$schema" = "https://www.schemastore.org/git-cliff.json"',
        `name = ${JSON.stringify(path.basename(preset.fileName, ".toml"))}`,
        `description = ${JSON.stringify(preset.description)}`,
        'version = "1.0.0"',
        "",
        preset.changelog.trim(),
        sharedChangelogOptions,
        "",
        renderGit(preset, sharedGit),
    ];

    if (remote !== "") {
        sections.push("", remote);
    }

    return `${sections.join("\n").trim()}\n`;
};

/**
 * @param {string} contents Unformatted TOML.
 * @param {string} outputPath Destination used for parser selection.
 * @param {import("prettier").Options} prettierConfig Repository formatting
 *   options.
 *
 * @returns {Promise<string>} Canonically formatted TOML.
 */
const formatPreset = async (contents, outputPath, prettierConfig) =>
    format(contents, {
        ...prettierConfig,
        filepath: outputPath,
    });

const main = async () => {
    const rootConfig = await readFile(rootConfigPath, "utf8");
    const sharedChangelogOptions = getSection(
        rootConfig,
        "    # Remove leading and trailing whitespace from the changelog content",
        "\n[git]"
    );
    const sharedGit = getSection(rootConfig, "[git]", "\n[remote]");
    const githubRemote = rootConfig
        .slice(rootConfig.indexOf("[remote]"))
        .trim();
    const staleFiles = [];
    const prettierConfig = (await resolveConfig(rootConfigPath)) ?? {};

    await mkdir(presetsDirectory, { recursive: true });

    for (const preset of presets) {
        const outputPath = path.join(presetsDirectory, preset.fileName);
        const expected = await formatPreset(
            renderPreset({
                preset,
                sharedChangelogOptions,
                sharedGit,
                githubRemote,
            }),
            outputPath,
            prettierConfig
        );

        if (checkOnly) {
            const actual = await readFile(outputPath, "utf8").catch(() => "");

            if (actual !== expected) {
                staleFiles.push(path.relative(repositoryRoot, outputPath));
            }
        } else {
            await writeFile(outputPath, expected);
        }
    }

    if (staleFiles.length > 0) {
        const staleFileList = staleFiles.map((file) => `- ${file}`).join("\n");

        throw new Error(
            `Generated presets are missing or stale:\n${staleFileList}\nRun npm run presets:generate.`
        );
    }

    console.log(
        checkOnly
            ? `${presets.length} generated presets are current.`
            : `Generated ${presets.length} presets.`
    );
};

await main();
