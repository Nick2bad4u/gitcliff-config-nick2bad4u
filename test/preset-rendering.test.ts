describe("published git-cliff presets", () => {
    it("keeps every standalone config generated from the canonical parser contract", async () => {
        expect.hasAssertions();

        const files = (await readdir(presetsDirectory))
            .filter((fileName) => fileName.endsWith(".toml"))
            .toSorted((left, right) => left.localeCompare(right, "en"));
        const configs = new Map<string, string>(
            await Promise.all(
                files.map(
                    async (fileName): Promise<readonly [string, string]> => [
                        fileName,
                        await readFile(
                            path.join(presetsDirectory, fileName),
                            "utf8"
                        ),
                    ]
                )
            )
        );
        const scopeCapture = [
            'scope = "',
            "$",
            '{1}"',
        ].join("");
        const unconventionalConfig = configs.get("unconventional.toml");

        expect(files).toStrictEqual(
            gitCliffPresetFileNames.toSorted((left, right) =>
                left.localeCompare(right, "en")
            )
        );
        expect(files).toHaveLength(17);
        expect(files).not.toContain("cliff.toml");
        expect(
            configs
                .values()
                .every((config) =>
                    config.startsWith(
                        "# This file is generated by npm run presets:generate."
                    )
                )
        ).toBe(true);
        expect(
            configs.values().every((config) => config.includes(scopeCapture))
        ).toBe(true);
        expect(
            configs
                .values()
                .every(
                    (config) =>
                        config.includes("Bump (") &&
                        config.includes("from.*?to")
                )
        ).toBe(true);
        expect(unconventionalConfig).toMatch(/conventional_commits\s+= false/v);
        expect(
            configs
                .entries()
                .filter(([fileName]) => fileName !== "unconventional.toml")
                .every(([, config]) =>
                    /conventional_commits\s+= true/v.test(config)
                )
        ).toBe(true);
        expect(getPresetExampleFileName("github.toml")).toBe("github.md");
        expect(() => getPresetExampleFileName("github.md")).toThrow(
            "Expected a .toml preset filename"
        );
    });

    it("renders every style with its defining layout and parsing behavior", async () => {
        expect.hasAssertions();

        const presetOutputs = await presetOutputsPromise;

        expect(presetOutputs.size).toBe(17);

        for (const changelog of presetOutputs.values()) {
            expectValidGeneratedMarkdown(changelog);
        }

        expectProviderPresets(presetOutputs);
        expectCoreStylePresets(presetOutputs);
        expectScopeAndStatisticsPresets(presetOutputs);
        expectUserFacingPresets(presetOutputs);
        expectCustomPresets(presetOutputs);
    });

    it("keeps every committed documentation example synchronized", async () => {
        expect.hasAssertions();

        const presetOutputs = await presetOutputsPromise;
        const presetFileNames = presetOutputs.keys().toArray();
        const expectedExampleFileNames = ["README.md"];

        for (const presetFileName of presetFileNames) {
            expectedExampleFileNames.push(
                getPresetExampleFileName(presetFileName)
            );
        }

        const sortedExpectedExampleFileNames =
            expectedExampleFileNames.toSorted((left, right) =>
                left.localeCompare(right, "en")
            );
        const actualExampleFileNames = (await readdir(presetExamplesDirectory))
            .filter((fileName) => fileName.endsWith(".md"))
            .toSorted((left, right) => left.localeCompare(right, "en"));

        expect(actualExampleFileNames).toStrictEqual(
            sortedExpectedExampleFileNames
        );

        const index = await readFile(
            path.join(presetExamplesDirectory, "README.md"),
            "utf8"
        );

        expect(index.replaceAll("\r\n", "\n")).toBe(
            renderPresetExamplesIndex(presetFileNames)
        );
        expect(remark().parse(index).type).toBe("root");

        for (const [presetFileName, changelog] of presetOutputs) {
            const exampleFileName = getPresetExampleFileName(presetFileName);
            const example = await readFile(
                path.join(presetExamplesDirectory, exampleFileName),
                "utf8"
            );
            const normalizedExample = example.replaceAll("\r\n", "\n");

            expect(normalizedExample).toBe(
                renderPresetExampleDocument(presetFileName, changelog)
            );

            expectValidGeneratedMarkdown(normalizedExample);
        }
    });
});
